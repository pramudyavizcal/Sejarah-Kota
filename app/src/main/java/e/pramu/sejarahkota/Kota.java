package e.pramu.sejarahkota;

public class Kota {
    private String namaKota;
    private String detailKota;
    private int photoKota;

    public String getNamaKota() {
        return namaKota;
    }

    public void setNamaKota(String namaKota) {
        this.namaKota = namaKota;
    }

    public String getDetailKota() {
        return detailKota;
    }

    public void setDetailKota(String detailKota) {
        this.detailKota = detailKota;
    }

    public int getPhotoKota() {
        return photoKota;
    }

    public void setPhotoKota(int photoKota) {
        this.photoKota = photoKota;
    }
}
